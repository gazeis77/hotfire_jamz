    	<p>When I first heard about the concept for “You're Dead” I was totally on board.  The idea of an electronic producer making a hard (link)Bebop record sounded right up my alley.  When people step outside of them selves and stretch their comfort zone.  Although this isn't completely out of the Flying Louts comfort zone because there is some precedence being the grand-nephew of Alice and John Coltrane.  This allows you to call in favors of sorts, like explaining I want to make a hard bop album about after being dead and having Herby Hancock jump on board.  And when you mix in long time conspirator and bass wizard Thundercat ones imagination can start to run wild.  I also pre ordered the vinyl boxset after I saw the insane artwork that came along with it.</p>

    	<p>The record itself is shorter than I wanted, clocking in around 35 minutes.  It also feature more lyrics than any other Flylo release before.  Kendricks feature was nothing short of fantastic while Snoops Dead Mans Tetris feature (and the whole song) really wrecked the whole flow of the album for me. Yet this is no small feat of a record. This is something only Steven Ellison could make, something the identity of Flying Louts could create. While I happily listened to this record over and over again in the car I felt like the project wasn't completely over.</p>


